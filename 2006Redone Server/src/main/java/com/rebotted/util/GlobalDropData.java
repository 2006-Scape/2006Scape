package com.rebotted.util;

public class GlobalDropData {

    private int id;
    private int amount;
    private int itemX;
    private int itemY;
    private int height;

    public int getId() {
        return id;
    }

    public int getAmount() {
        return amount;
    }

    public int getItemX() {
        return itemX;
    }

    public int getItemY() {
        return itemY;
    }

    public int getHeight() {
        return height;
    }
}
