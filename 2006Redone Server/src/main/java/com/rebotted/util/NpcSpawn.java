package com.rebotted.util;

public class NpcSpawn {
    int id;
    int x;
    int y;
    int height;
    int walk;
    int maxHit;
    int attack;
    int strength;

    public int getId() {
        return id;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getHeight() {
        return height;
    }

    public int isWalk() {
        return walk;
    }

    public int getMaxHit() {
        return maxHit;
    }

    public int getAttack() {
        return attack;
    }

    public int getStrength() {
        return strength;
    }
}
